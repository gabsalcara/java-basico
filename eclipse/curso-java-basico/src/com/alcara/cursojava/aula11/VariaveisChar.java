package com.alcara.cursojava.aula11;

public class VariaveisChar {

	public static void main(String[] args) {

		// Representa a saida como texto		
		char o = 'o';
		char i = 'i';
		
		System.out.println("" + o + i);
		
		// Representa a saida como numérico
		char O = 111;
		char I = 105;		
		
		System.out.println(O + I);
		
		// Representa a saida como caracteres especiais
		char caracteres = 0X00E1;
		System.out.println("" + O + I + caracteres);
		
	}

}
